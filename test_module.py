#!/usr/bin/python3

def add(a: int, b: int) -> float:
    """
    This function add two integers
    Return Float
    """
    return a+b


def sub(a: int, b: int) -> float:
    """
    This function subtract two integers
    Return Float
    """
    return a-b


def multiply(a: int, b: int) -> float:
    """
    This function multiply two integers
    Return Float
    """
    return a*b


def div(a: int, b: int) -> float:
    """
    This function divide two integers
    Return Float
    """
    return a/b
